package basics;

public class SpecialNumbers {
    public static void main(String[] args) {
        /*
        Numbering systems supported in Java
        1. Decimal
        2. Binary
        3. Octal
        4. Hexadecimal
        */
    
        // sample of a binary number
        int binary  = 0b001;
        
        // sample of a octal number
        int octal = 011;

        // sample of a hexadecimal number
        int hex = 0x01f;

        System.out.println(binary);
        System.out.println(octal);
        System.out.println(hex);

    }

}
